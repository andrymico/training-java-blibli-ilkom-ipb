
public class SwitchCase {

    public static void main(String[] args) {

        int x = 1;

        switch (x) {
            case 1:
                System.out.println(
                        "this is one");
                break;
            case 2:
                System.out.println(
                        "this is one");
                break;
            default:
                System.out.println(
                        "not one or two");
        }

    }

}
